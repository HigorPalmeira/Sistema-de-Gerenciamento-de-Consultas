    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        int idx = tblEspecialidades.getSelectedRow();
        
        if (idx > -1) {
            
            UUID idEspecialidade = UUID.fromString(String.valueOf(tblEspecialidades.getValueAt(idx, 0)));
            String descricaoEspecialidade = String.valueOf(tblEspecialidades.getValueAt(idx, 1));
            
            frmEditarEspecialidade frmEditarEspecialidade = new frmEditarEspecialidade(this.especialidadeService);
            frmEditarEspecialidade.setIdEspecialidade(idEspecialidade);
            frmEditarEspecialidade.setDescricaoEspecialidade(descricaoEspecialidade);
            frmEditarEspecialidade.setVisible(true);
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed

        int idx = tblEspecialidades.getSelectedRow();
        
        if (idx == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma especialidade na tabela para poder excluí-la!", "Especialidade não selecionada", JOptionPane.ERROR_MESSAGE);
            
        } else {
            
            int opcao = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir esta especialidade?", "Tem certeza?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if (opcao == JOptionPane.YES_OPTION) {
                
                UUID idEspecialidade = UUID.fromString( String.valueOf(tblEspecialidades.getValueAt(idx, 0)) );
                
                if (this.especialidadeService.deletarEspecialidade( idEspecialidade )) {
                    
                    JOptionPane.showMessageDialog(this, "A especialidade foi deletada com sucesso!", "Sucesso na deleção", JOptionPane.INFORMATION_MESSAGE);
                    
                } else {
                    
                    JOptionPane.showMessageDialog(this, "A especialidade não foi deletada!", "Falha na deleção", JOptionPane.ERROR_MESSAGE);
                    
                }
            
            }
            
        }

    }//GEN-LAST:event_btnDeletarActionPerformed

