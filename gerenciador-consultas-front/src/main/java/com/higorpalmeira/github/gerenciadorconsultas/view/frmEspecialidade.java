    private void pesquisar_especialidade() {
        if (txtPesquisa.getText() == null || txtPesquisa.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "É necessário a descrição da especialidade para pesquisa!", "Falha na pesquisa", JOptionPane.ERROR_MESSAGE);
        
        } else {
            
            List<SaidaSimplesEspecialidadeDto> listaEspecialidadeDto = this.especialidadeService.buscarSaidaSimplesEspecialidadeDto(txtPesquisa.getText().trim());
            
            DefaultTableModel dtm = (DefaultTableModel) tblEspecialidades.getModel();
            dtm.setNumRows(0);
            
            if (!listaEspecialidadeDto.isEmpty()) {
                
                for (SaidaSimplesEspecialidadeDto especialidadeDto : listaEspecialidadeDto) {
                    
                    Object[] obj = { especialidadeDto.getId(), especialidadeDto.getDescricao(), especialidadeDto.getMedicosAssociados() };
                
                    dtm.addRow(obj);
                    
                }
            
            }
            
        }
    }
    
    private void listarEspecialidade() {
        
        if (this.carregarTabela) {
            
            this.carregarTabela = false;
            
            List<SaidaSimplesEspecialidadeDto> listaEspecialidade = especialidadeService.listarSaidaSimplesEspecialidadeDto();

            DefaultTableModel dtm = (DefaultTableModel) tblEspecialidades.getModel();
            dtm.setNumRows(0);

            if (!listaEspecialidade.isEmpty()) {

                for (SaidaSimplesEspecialidadeDto especialidadeDto : listaEspecialidade) {

                    Object[] obj = { especialidadeDto.getId(), especialidadeDto.getDescricao(), especialidadeDto.getMedicosAssociados() };

                    dtm.addRow(obj);

                }
                
            }
            
        }
        
        
    }
    
